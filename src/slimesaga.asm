END_OF_VARIABLES:

section "hvariables", hram
storage: DS 1


section "header", rom0[$0100]
entrypoint:
	di
	jr start
	ds ($0150 - @), 0


section "main", rom0[$0150]
start:
	ld a, [rLY]			; wait for VBLANK to turn off LCD
	cp SCRN_Y
	 jr nz, start
	xor a
	ld [rLCDC], a		; disable LCD

	ld a, %11100100 	; light to dark
	ld [rBGP], a		; update background/tile palette
	ld [rOBP0], a		; update sprite palette
	ld [rOBP1], a		; update sprite palette

; set up RAM variables
	ld hl, map_w
	ld bc, END_OF_VARIABLES - START_OF_VARIABLES
	xor a
.reset:
	ld [hl+], a
	dec bc
	bit 7, b
	 jr z, .reset

; set up player start position
	ld a, 1
	ld [player_x + 1], a
	inc a
	ld [player_y + 1], a

; reset OAM
	ld c, OAM_COUNT * 4		; 40 OAM entries
	ld hl, _OAMRAM + OAMA_Y
	xor a				; set a to zero
.init_oam:
	ld [hl+], a			; set OAM Y to 0
	dec c				; run 40 times altogether
	 jr nz, .init_oam

; load tile data into VRAM
	ld de, tile_data
	ld hl, _VRAM8800
	ld b, 16
.loop:
	ld a, [de]
	inc de
	ld [hl+], a
	ld a, high(tile_end)
	dec b
	 ;jr z, main
	cp d
	 jr nz, .loop
	ld a, low(tile_end)
	cp e
	 jr nz, .loop

	call load_player_sprites
	call load_tilemap

	ld a, LCDCF_ON | LCDCF_BGON | LCDCF_BG9800 | LCDCF_BG8800 | LCDCF_OBJON | LCDCF_OBJ16
	ld [rLCDC], a

main:
.vblank:
	ld a, [rLY]
	cp SCRN_Y
	 jr nz, .vblank
	
	call update_camera		; [camera.asm]
	call draw_player		; [player.asm]

	ld a, P1F_GET_DPAD		; prepare to read DPAD from key port
	ld [rP1], a				; send request to read DPAD status
	ldh a, [rP1]			; wait state
	ldh a, [rP1]			; DPAD status in a

	ld hl, player_frame
	cp $EF
	 jr nz, .check_keys
		ld [hl], a
.check_keys:
	inc [hl]				; increase player animation counter
	swap a
	push af
		and PADF_UP			;
		 call z, move_up	; [tilemap.asm]
	pop af
	push af
		and PADF_DOWN		;
		 call z, move_down	; [tilemap.asm]
	pop af
	push af
		and PADF_LEFT		;
		 call z, move_left	; [tilemap.asm]
	pop af
	and PADF_RIGHT		;
	 call z, move_right	; [tilemap.asm]

	ld a, P1F_GET_NONE		; disable key polling
	ld [rP1], a				;
	jr main

